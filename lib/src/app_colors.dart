    grey: AppColors.neutralgreyblack20,
    greyDarker: AppColors.neutralgreyblack90,
    contrastOpacityS: AppColors.neutralshadowdark20,
    contrastOpacityM: AppColors.neutralshadowdark40,
    basicContrastOpposite: AppColors.neutralgreyblack90,
    nightShow: AppColors.accentbleubleuBase,
  );
}

// Extension for easier theme color access
extension BuildContextThemeExtension on BuildContext {
  AppThemeColors get colors => Theme.of(this).extension<AppThemeColors>()!;
}
